export function IconFromName({
  name,
  className = "w-5 h-5",
}: {
  name: string;
  className?: string;
}) {
  const Icon = iconMap[name];
  if (!Icon) return <span className={className} aria-hidden="true">{name}</span>;
  return <Icon className={className} aria-hidden="true" />;
}
